					<span class="drop-cap round">B</span>rick making remains an important industry in many rural and peri-urban areas, but
                       the income it provides can be a very marginal one. Often there are large numbers of
                       producers and the quality of bricks produced can vary greatly. Stabilised Soil Brick
                      technology (also known as Stabilized Compressed Earth Block (SCEB)) allows
                      producers to easily manufacture higher quality bricks and as a result receive a higher
                      income for themselves. 
                      </p>
                      <p>
                      Stabilised soil bricks technology offers a cost effective, environmentally sound
                      masonry system. The product has a wide application in construction and is
                      manufactured by compacting earth (murram/sub-soil) mixed
                      with a stabilizer such as cement or lime. A number of manual
                       machines are available which compact the soil to attain
                        dense, even sized masonry. The bricks are then laid out in the mortar costs. The bricks have four key advantages over
                          traditional bricks:<br>
                           <strong>Advantages:</strong><br>
                         <i><strong>1) Build Quality:</strong></i>
                           SSB allows users to produce uniform blocks of greater strength than typical fired blocks that provide better thermal insulation.<br>
                          <i><strong>2) Financial:</strong></i> <br>
                             The total cost of building a structure with SSB is 20%-30% cheaper than building with fired bricks because far less mortar is required,
                            structures don’t have to be plastered, and bricks can be made on site so transportation costs are minimized.<br>
                            <i><strong> 3) Environmental:</strong></i><br>
                            Because SSBs are cured in the sun, the need for fuel wood is eliminated, helping curb deforestation rates.<br>
                       <i><strong> 4) Appearance:</strong></i> <br>
                         The bricks have an appealing aesthetic with an elegant profile and uniform size that doesn’t require plastering</p>
